package com.techninja01.practical_7;

public class IcecreamList {
    int images;
    String names;

    public IcecreamList(int images, String names) {
        this.images = images;
        this.names = names;
    }

    public int getImages() {
        return images;
    }

    public void setImages(int images) {
        this.images = images;
    }

    public String getNames() {
        return names;
    }

    public void setNames(String names) {
        this.names = names;
    }
}
